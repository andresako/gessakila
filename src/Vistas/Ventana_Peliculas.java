    private boolean checkearMinimosRequeridos() {
        if (cmbPeliLenguaje.getSelectedIndex() < 0) {
            MT.mostrarError("Rellene el lenguaje");
            return false;
        }
        if (txtPeliTitulo.getText().equals("")) {
            MT.mostrarError("Rellene el t√≠tulo");
            return false;
        }
        if (txtPeliRCost.getText().equals("")) {
            MT.mostrarError("Rellene el coste de alquiler");
            return false;
        }
        if (txtPeliRRate.getText().equals("")) {
            MT.mostrarError("Rellene el porcentaje de alquiler");
            return false;
        }
        if (txtPeliRTime.getText().equals("")) {
            MT.mostrarError("Rellene el tiemp de alquiler");
            return false;
        }

        return true;
    }

    private void rellenarPeli(Film filmCt) {

        String descripcion = txtPeliDescripcion.getText();
        if (!descripcion.equals("")) {
            filmCt.setDescription(descripcion);
        }

        try {
            String duracion = txtPeliDuracion.getText();
            if (!duracion.equals("")) {
                filmCt.setLength(Short.parseShort(duracion));
            }
        } catch (Exception ex) {
            MT.mostrarError("Ingese la duracion de la peli correctamente");
        }

        if (cmbPeliRating.getSelectedIndex() >= 0) {
            String rating = cmbPeliRating.getSelectedItem().toString();
            filmCt.setRating(rating);
        }

        String features = "";
        if (chBehind.isSelected()) {
            features += "Behind the Scenes";
        }
        if (chComentarios.isSelected()) {
            if (!features.equals("")) {
                features += ",";
            }
            features += "Commentaries";
        }
        if (chDeleted.isSelected()) {
            if (!features.equals("")) {
                features += ",";
            }
            features += "Deleted Scenes";
        }
        if (chTrailers.isSelected()) {
            if (!features.equals("")) {
                features += ",";
            }
            features += "Trailers";
        }
        filmCt.setSpecialFeatures(features);
    }

    private void rellenarFeatures(String specialFeatures) {

        chBehind.setSelected(false);
        chComentarios.setSelected(false);
        chDeleted.setSelected(false);
        chTrailers.setSelected(false);
        try {
            String feaArray[] = specialFeatures.split(",");

            for (String ct : feaArray) {
                if (ct.equalsIgnoreCase("Trailers")) {
                    chTrailers.setSelected(true);
                }
                if (ct.equalsIgnoreCase("Commentaries")) {
                    chComentarios.setSelected(true);
                }
                if (ct.equalsIgnoreCase("Deleted Scenes")) {
                    chDeleted.setSelected(true);
                }
                if (ct.equalsIgnoreCase("Behind the Scenes")) {
                    chBehind.setSelected(true);
                }
            }
        } catch (NullPointerException ex) {

        }
    }
