    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        if (checkearMinimosRequeridos()) {

            Language lng = ((LenguajeCombo) cmbPeliLenguaje.getSelectedItem()).getLanguage();
            String title = txtPeliTitulo.getText();
            byte rDuration = Byte.parseByte(txtPeliRTime.getText());
            BigDecimal rRate = BigDecimal.valueOf(Double.parseDouble(txtPeliRRate.getText()));
            BigDecimal rCost = BigDecimal.valueOf(Double.parseDouble(txtPeliRCost.getText()));
            Film filmCt;
            if (nuevoRegistro) {
                filmCt = new Film(lng, title, rDuration, rRate, rCost, new Date());

                rellenarPeli(filmCt);

                PT.addFilm(filmCt);
            } else {
                filmCt = PT.getPeli(PeliActual);

                filmCt.setLanguageByLanguageId(lng);
                filmCt.setTitle(title);
                filmCt.setRentalDuration(rDuration);
                filmCt.setRentalRate(rRate);
                filmCt.setReplacementCost(rCost);
                rellenarPeli(filmCt);

                PT.updFilm(PeliActual, filmCt);
            }
            habilitarEdicion(false);
            mostrarDatos(PeliActual);
        }

    }//GEN-LAST:event_btnOkActionPerformed

