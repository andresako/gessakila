    private void SetUi() {
        AT = new ActorTools();
        MT = new MyTools();
        AT.RellenarActores();
        
        if (AT.ContarActores() > 0) {
            MostrarDatos(1);
        } else {
            MT.mostrarError("No hay Actores que mostrar");
            ActorActual = 1;
            btnNuevoActionPerformed(null);
            btnCancel.setEnabled(false);
        }
    }
    
    private void MostrarDatos(int act) {
        ActorActual = act;
        txtID.setText("" + AT.getActor(act).getActorId());
        txtNombre.setText(AT.getActor(act).getFirstName());
        txtApellido.setText(AT.getActor(act).getLastName());
        txtLastUpd.setText("" + AT.getActor(act).getLastUpdate());
        txtRegAct.setText("" + ActorActual);
        txtRegMax.setText("" + AT.ContarActores());
        
        if (ActorActual == 1) {
            btnAnt.setEnabled(false);
            btnPrime.setEnabled(false);
        } else {
            btnAnt.setEnabled(true);
            btnPrime.setEnabled(true);
        }
        if (ActorActual == AT.ContarActores()) {
            btnSig.setEnabled(false);
            btnUltim.setEnabled(false);
        } else {
            btnSig.setEnabled(true);
            btnUltim.setEnabled(true);
        }
        nuevoActor = false;
        
    }
    
    private void habilitarEdicion(boolean b) {
        txtNombre.setEnabled(b);
        txtApellido.setEnabled(b);
        
        btnOk.setEnabled(b);
        btnCancel.setEnabled(b);
        
        btnPrime.setEnabled(!b);
        btnAnt.setEnabled(!b);
        btnSig.setEnabled(!b);
        btnUltim.setEnabled(!b);
        
        btnNuevo.setEnabled(!b);
        btnBorrar.setEnabled(!b);
        btnEdit.setEnabled(!b);
        btnActorDe.setEnabled(!b);
        
    }
    
    private boolean checkearCampos() {
        if (txtNombre.getText().equals("")) {
            MT.mostrarError("Rellena el nombre");
            return false;
        }
        if (txtApellido.getText().equals("")) {
            MT.mostrarError("Rellena el apellido");
            return false;
        }
        return true;
    }
