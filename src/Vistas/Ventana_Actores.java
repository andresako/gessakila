    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (checkearCampos()) {
            Actor act;
            if (nuevoActor) {
                act = new Actor(txtNombre.getText(), txtApellido.getText(), new Date());
                AT.addActor(act);
                MostrarDatos(AT.ContarActores());
            } else {
                act = AT.getActor(ActorActual);
                act.setFirstName(txtNombre.getText());
                act.setLastName(txtApellido.getText());
                act.setLastUpdate(new Date());
                AT.updActor(ActorActual, act);
            }
        }
        
        habilitarEdicion(false);
    }//GEN-LAST:event_btnOkActionPerformed

